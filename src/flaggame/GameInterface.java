    public void exportInformation()
    {
        try
        {
            String outputFile = "FlagGameResults.txt";
            PrintWriter outputWriter;
            outputWriter = new PrintWriter(outputFile);
            String date = GameResult.studentResult.dateAndTimeCompilation();
            String outputMessage =(studentName + "   " + studentScore + "/" + numOfFlagsChosen + "   " + date );
            GameData.quizResults.add(outputMessage);
            for (String i: GameData.quizResults)
            {
                outputWriter.println(i);
            }
            outputWriter.close();
          
        }
        catch(Exception noFileFound)
        {
            System.out.println("Error Writing File");
        }
    }
    
    public void numOfFlags()
    {
        String numberOfFlags = JOptionPane.showInputDialog("How many flags would you like to quiz on? (Choose between 1 and 197)");
        numOfFlagsChosen = Integer.parseInt(numberOfFlags);
        if (numOfFlagsChosen > 197)
        {
            numOfFlags();
        }
        state = true;
        Thread timer = new  Thread()
        {
           public void run()
           {
               
               for(;;)
               {
                  if(state == true)
                    {
                        try
                        {
                            sleep(1);
                            if(milliseconds>1000)
                            {
                                milliseconds = 0;
                                seconds++;
                            }
                            if(seconds>60)
                            {
                                milliseconds = 0;
                                seconds = 0;
                                minutes++;
                            }
                            if(minutes>60)
                            {
                                milliseconds = 0;
                                seconds = 0;
                                minutes = 0;
                                hours++;
                            }
                            
                           
                           milliseconds++; 
                        }
                        catch(Exception e)
                        {
                            
                        }
                    } 
                  else
                  {
                      break;
                  }
               }
                   
           }
        };
       
    }
        
