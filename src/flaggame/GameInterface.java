    private void submitQuestionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitQuestionActionPerformed
       boolean answerStatus;
       
       if(studentAnswer == answerPlace)
       {
           answerStatus = true;
           studentScore = studentScore + 1;
           Status.setText("Correct");
           
       }
       else
       {
           answerStatus = false;
           Status.setText("Incorrect");
       }
       
       if(questionCounter < numOfFlagsChosen )
       {
           questionCounter = questionCounter + 1;   
           nextQuestion();
       }
       else
       {
        exportInformation();
        this.setVisible(false);
        new Score(numOfFlagsChosen, studentScore).setVisible(true);
       }
       String score = String.valueOf(questionCounter);
       numberOfFlagsScor.setText(score);
       String totNumFlags = String.valueOf(numOfFlagsChosen);
       scoreOutOf.setText(totNumFlags);
       //jProgressBar1.setValue(questionCounter + 1);
       //ProgressBar1.setMaximum(numOfFlagsChosen);
    }//GEN-LAST:event_submitQuestionActionPerformed
    
